// --- Populate Regions ---
document.getElementById("country").addEventListener("change", function() {
  const regionDropdown = document.getElementById("region");
  regionDropdown.innerHTML = '<option value="">Select a region</option>';
  const hospitalDropdown = document.getElementById("hospital");
  hospitalDropdown.innerHTML = '<option value="">Select a hospital</option>';
  const doctorDropdown = document.getElementById("doctor");
  doctorDropdown.innerHTML = '<option value="">Select a doctor</option>';
  document.getElementById("contactHospital").value = "";
  document.getElementById("location").value = "";

  if (this.value && data[this.value]) {
    Object.keys(data[this.value]).forEach(region => {
      const option = document.createElement("option");
      option.value = region;
      option.textContent = region;
      regionDropdown.appendChild(option);
    });
  }
});

// --- Populate Hospitals ---
document.getElementById("region").addEventListener("change", function() {
  const hospitalDropdown = document.getElementById("hospital");
  hospitalDropdown.innerHTML = '<option value="">Select a hospital</option>';
  const doctorDropdown = document.getElementById("doctor");
  doctorDropdown.innerHTML = '<option value="">Select a doctor</option>';
  document.getElementById("contactHospital").value = "";
  document.getElementById("location").value = "";

  const country = document.getElementById("country").value;
  if (data[country] && data[country][this.value]) {
    Object.keys(data[country][this.value]).forEach(hospital => {
      const option = document.createElement("option");
      option.value = hospital;
      option.textContent = hospital;
      hospitalDropdown.appendChild(option);
    });
  }
});

// --- Auto-fill Contact, Location & Load Doctors ---
document.getElementById("hospital").addEventListener("change", function() {
  const country = document.getElementById("country").value;
  const region  = document.getElementById("region").value;
  const hospital = this.value;

  if (data[country] && data[country][region] && data[country][region][hospital]) {
    const hospitalData = data[country][region][hospital];
    document.getElementById("contactHospital").value = hospitalData.contact;
    document.getElementById("location").value = hospitalData.location;

    const doctorDropdown = document.getElementById("doctor");
    doctorDropdown.innerHTML = '<option value="">Select a doctor</option>';
    hospitalData.doctors.forEach(doctor => {
      const option = document.createElement("option");
      option.value = doctor;
      option.textContent = doctor;
      doctorDropdown.appendChild(option);
    });
  }
});
